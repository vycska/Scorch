		case STATE_ROUND_END:
			game.round+=1;
			if(game.round>5) {
				write+=2;
				State=STATE_END;
			}
			else {
				write+=2;
				State=STATE_ROUND_BEGIN;
			}
			break;
		case STATE_END:
			draw=1;
			write+=2;
			State=STATE_START;
			break;
		case STATE_PLAYER_POSITION:
			draw+=1;
			tank[0].pADCdata2=ADC0_In();
			State=STATE_PLAYER_ANGLE;
			break;
