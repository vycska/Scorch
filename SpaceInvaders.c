void SysTick_Handler(void) {
	switch(State) {
		case STATE_EXPLOSION:
			if(explosion.current<EXPLOSION_SIZE) {
				explosion.current+=3;
				draw+=1;
			}	
			else {
				LED_OnOff(0);
				State=STATE_ROUND_END;
				draw=1;
				write+=2;
			}
			break;
		case STATE_COMP_POSITION:
			draw+=1;
			if(tank[1].pos!=tank[1].fpos) {
				tank[1].pos+=(tank[1].pos<tank[1].fpos?(MIN(1,tank[1].fpos-tank[1].pos)):(MAX(-1,tank[1].fpos-tank[1].pos)));
			}
			else {
				temp=Tank_SelectAngle();
				if(temp==-1) {
					laser.weapon=2;
					tank[1].fangle=90;
				}
				else {
					laser.weapon=1;
					tank[1].fangle=temp*10;
				}
				State=STATE_COMP_ANGLE;
			}
			break;
		case STATE_COMP_ANGLE:
			draw+=1;
			if(tank[1].angle!=tank[1].fangle) {
				tank[1].angle+=(tank[1].angle<tank[1].fangle?(MIN(10,tank[1].fangle-tank[1].angle)):(MAX(-10,tank[1].fangle-tank[1].angle)));
			}
			else {	
				Tank_LaserPath(1);
				laser.current=0;
				State=STATE_COMP_LASER;
				Sound_Play(shoot,4080);
			}
			break;
		case STATE_PLAYER_LASER:
		case STATE_COMP_LASER:
			if(laser.current==laser.n) {
				if(laser.path[laser.n-1].x>=84-tank[1].width && laser.path[laser.n-1].x<84 && laser.path[laser.n-1].y>=tank[1].pos && laser.path[laser.n-1].y<=tank[1].pos+tank[1].height) {
					game.win=1;
					game.wPlayer+=1;
					explosion.current=3;
					draw+=2;
					State=STATE_EXPLOSION;
					Sound_Play(explode,4080);
				}
				else if(laser.path[laser.n-1].x>=0 && laser.path[laser.n-1].x<=4 && laser.path[laser.n-1].y>=tank[0].pos && laser.path[laser.n-1].y<=tank[0].pos+tank[0].height) {
					game.win=2;
					game.wComp+=1;
					explosion.current=6;
					draw+=2;
					State=STATE_EXPLOSION;
					Sound_Play(explode,4080);
				}
				else {
					draw+=2;
					game.shot+=1;
					if(laser.weapon==2) {
						if(game.n[1]>game.n[0]) {
							game.n[1]=MAX(1.0,game.n[1]-1.0);
						}
						else if(game.n[0]>game.n[1]) {
							game.n[0]=MAX(1.0,game.n[0]-1.0);
						}
					}
					Sound_Play(bump,982);
					if(State==STATE_PLAYER_LASER) {
						tank[1].fpos=rand()%(48-tank[1].height);
						State=STATE_COMP_POSITION;
						LED_OnOff(2);
					}
					else {
						tank[0].pADCdata1=ADC0_In();
						State=STATE_PLAYER_POSITION;
						LED_OnOff(1);
					}
				}
			}
			else {
				laser.current+=MIN(LASER_SPEED,laser.n-laser.current);
				draw+=1;
			}
			break;
		case STATE_PLAYER_POSITION:
			ADCdata=ADC0_In();
			temp=tank[0].pos;
			if(ADCdata>tank[0].pADCdata1)
				tank[0].pos+=(double)(ADCdata-tank[0].pADCdata1)/(4095.0-tank[0].pADCdata1)*(48-tank[0].height-tank[0].pos);
			else if(ADCdata<tank[0].pADCdata1)
				tank[0].pos-=(double)(tank[0].pADCdata1-ADCdata)/tank[0].pADCdata1*tank[0].pos;
			tank[0].pos=MIN(48-tank[0].height,tank[0].pos);
			tank[0].pos=MAX(0,tank[0].pos);
			if(temp!=tank[0].pos) {
				draw+=1;
				tank[0].pADCdata1=ADCdata;
			}
			break;
		case STATE_PLAYER_ANGLE:
			ADCdata=ADC0_In();
			temp=tank[0].angle;
			if(ADCdata>tank[0].pADCdata2)
				tank[0].angle+=(int)((double)(ADCdata-tank[0].pADCdata2)/(4095.0-tank[0].pADCdata2)*(18-tank[0].angle/10))*10;
			else if(ADCdata<tank[0].pADCdata2)
				tank[0].angle-=(int)((double)(tank[0].pADCdata2-ADCdata)/tank[0].pADCdata2*tank[0].angle/10)*10;
			tank[0].angle=MIN(180,tank[0].angle);
			tank[0].angle=MAX(0,tank[0].angle);
			if(temp!=tank[0].angle) {
				draw+=1;
				tank[0].pADCdata2=ADCdata;
			}
			break;
	}
}

void Timer2_Init(unsigned long int f) { 
	volatile unsigned long int delay;

	SYSCTL_RCGCTIMER_R|=0x04;													// 0) activate timer2
	delay=SYSCTL_RCGCTIMER_R;

	TIMER2_CTL_R=0x0;																	// 1) disable timer2A during setup
	TIMER2_CFG_R=0x0;																	// 2) configure for 32-bit mode
	TIMER2_TAMR_R=0x2;																// 3) configure for periodic mode, default down-count settings
	TIMER2_TAILR_R=1000000000L/f/12.5-1;							// 4) reload value
	TIMER2_TAPR_R=0x0;																// 5) bus clock resolution
	TIMER2_ICR_R=0x1;																	// 6) clear timer2A timeout flag
	TIMER2_IMR_R=0x1;																	// 7) arm timeout interrupt
	NVIC_PRI5_R=(NVIC_PRI5_R&0x00FFFFFF)|0x80000000;	// 8) priority 4
	NVIC_EN0_R|=1<<23;																// 9) enable IRQ 23 in NVIC
	//TIMER2_CTL_R=0x1;																	// 10) enable timer2A
}

void Timer2A_Handler(void) {
	TIMER2_ICR_R=0x1;   // acknowledge timer2A timeout
	if(sound.count>0) {
		DAC_Out(sound.wave[sound.index]>>4);
		sound.index++;
		sound.count--;
	}
	else TIMER2_CTL_R&=~0x1;
}

