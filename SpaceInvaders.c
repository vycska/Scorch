void Nokia5110_Line(int x1,int y1,int x2,int y2) {
	int i,j,
		f;
	double k,b;
	if(x1==x2) {
		for(i=MIN(y1,y2);i<=MAX(y1,y2);i++)
			Nokia5110_Point(x1,i);
	}
	else {
		k=(double)(y2-y1)/(x2-x1);
		b=y1-k*x1;
		f=ABS(x2-x1)>ABS(y2-y1);
		for(i=(f?MIN(x1,x2):MIN(y1,y2));i<=(f?MAX(x1,x2):MAX(y1,y2));i++) {
			j=(f?(k*i+b+0.5):((i-b)/k+0.5));
			Nokia5110_Point(f?i:j,f?j:i);
		}
	}
}

void Nokia5110_Point(int x,int y) {
	Screen[y/8*84+x]|=(1<<(y%8));
}

void Tank_LaserPath(int player) {
	int j,
		x,y,
		xCollision,yCollision,
		fCollision,fReflection,
		i,r;
	double k,b,k1,b1,k2;
	
	laser.path[0].x=(player==0?(tank[player].width):(83-tank[player].width))+(player==0?(1):(-1))*tank[player].length*sin(PI-RAD(tank[player].angle))+0.5;
	laser.path[0].y=tank[player].pos+tank[player].height/2+tank[player].length*cos(PI-RAD(tank[player].angle))+0.5;
	laser.n=1;
	
	if(tank[player].angle==0 || tank[player].angle==180) {
		for(i=laser.path[0].y+(tank[player].angle==0?(-1):(1));i>=0 && i<=47;i=(tank[player].angle==0?(i-1):(i+1))) {
			laser.path[laser.n].x=laser.path[0].x;
			laser.path[laser.n].y=i;
			laser.n+=1;
		}
	}
	else {
		k1=tan((player==0?(-1):(1))*PI/2+(player==0?(1):(-1))*RAD(tank[player].angle));
		b1=laser.path[0].y-k1*laser.path[0].x;
		
		if(interface.x1==interface.x2) {
			xCollision=interface.x1;
			yCollision=k1*xCollision+b1;
		}
		else if(k1!=interface.k) {
				xCollision=(interface.b-b1)/(k1-interface.k);
				yCollision=k1*xCollision+b1;
		}
		else xCollision=yCollision=0;
		
		fCollision=(xCollision>=MIN(interface.x1,interface.x2) && xCollision<=MAX(interface.x1,interface.x2) && yCollision>=0 && yCollision<=47);
		fReflection=0;

		for(k=k1,b=b1,x=laser.path[0].x;x>=0 && x<=83;x=(player==0?(fReflection?(x-1):(x+1)):(fReflection?(x+1):(x-1)))) { //+(player==0?(1):(-1))
			y=k*x+b+0.5;
			if(ABS(y-laser.path[laser.n-1].y)>1) {
				for(j=(y>laser.path[laser.n-1].y?(laser.path[laser.n-1].y+1):(laser.path[laser.n-1].y-1));j>=0 && j<=47 && (y>laser.path[laser.n-1].y?(j<=y):(j>=y)) && (j-b)/k>=0 && (j-b)/k<=83;j=(y>laser.path[laser.n-1].y?(j+1):(j-1))) {
					laser.path[laser.n].y=j;
					laser.path[laser.n].x=(j-b)/k+0.5;
					laser.n++;
				}
			}
			else if(y>=0 && y<=47) {
				laser.path[laser.n].x=x;
				laser.path[laser.n].y=y;
				laser.n++;
			}
			
			if(fCollision && x==xCollision) {
				if(laser.weapon==2)
					break;
				i=(player==0?(180-tank[player].angle-interface.angle):(interface.angle-tank[player].angle));
				if(ABS(i)>DEG(asin(1/game.n[player]))) { //total reflection
					fReflection=1;
					k=k2=tan((player==0?(-1):(1))*PI/2+(player==0?(1):(-1))*RAD(tank[player].angle+2*i));
					b=yCollision-k2*xCollision;
				}
				else { //refraction
					r=DEG(asin(game.n[player]*sin(RAD(i))/game.n[player%2==0]));
					k=k2=tan((player==0?(-1):(1))*PI/2+(player==0?(1):(-1))*RAD(tank[player].angle+i-r));
					b=yCollision-k2*xCollision;
				}
			}
		}
	}
}

int Tank_SelectAngle(void) {
	int j,
		x1,y1,x2,y2,y3,
		i,r,
		diff,min=-1,
		angles[18];
	double k,b;
	for(j=0;j<18;j++)
		angles[j]=0;
	for(j=0;j<2*game.round+game.shot;j++)
		angles[rand()%17+1]=1;
	for(j=0;j<18;j++)
		if(angles[j]) {
			x1=83-tank[1].width-tank[1].length*sin(PI-RAD(j*10))+0.5;
			y1=tank[1].pos+tank[1].height/2+tank[1].length*cos(PI-RAD(j*10))+0.5;
			k=tan(PI/2-RAD(j*10));
			b=y1-k*x1;
			
			if(interface.x1==interface.x2) {
				x2=interface.x1;
				y2=k*x2+b;
			}
			else if(k!=interface.k) {
				x2=(interface.b-b)/(k-interface.k);
				y2=k*x2+b;
			}
			else x2=y2=0;
			
			if(x2>=MIN(interface.x1,interface.x2) && x2<=MAX(interface.x1,interface.x2) && y2>=0 && y2<=47) {
				i=interface.angle-j*10;
				if(ABS(i)<DEG(asin(1/game.n[1]))) {
					r=DEG(asin(game.n[1]*sin(RAD(i))/game.n[0]));
					y3=y2-tan(PI/2-RAD(j*10+i-r))*x2;
					diff=ABS(y3-tank[0].pos-tank[0].height/2);
					if(min==-1 || diff<min) min=j;
				}
			}
		}
	return min;
}

